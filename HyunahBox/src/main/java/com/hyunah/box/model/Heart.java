package com.hyunah.box.model;

public class Heart {
	
	private String heartMovieCode;
	private String id;
	private String movieCode;
	
	
	public String getHeartMovieCode() {
		return heartMovieCode;
	}
	public void setHeartMovieCode(String heartMovieCode) {
		this.heartMovieCode = heartMovieCode;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMovieCode() {
		return movieCode;
	}
	public void setMovieCode(String movieCode) {
		this.movieCode = movieCode;
	}
	
	
	
}

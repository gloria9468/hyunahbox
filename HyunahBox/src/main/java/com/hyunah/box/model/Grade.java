package com.hyunah.box.model;

public class Grade {

	private String id;
	private String grade;
	private int gradePer;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public int getGradePer() {
		return gradePer;
	}
	public void setGradePer(int gradePer) {
		this.gradePer = gradePer;
	}
	
	
}
